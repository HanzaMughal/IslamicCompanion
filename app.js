// Moon phase by hijri day (rough approximation)
function moonPhaseIcon(hijriDay) {
    const d = parseInt(hijriDay);
    if (d <= 1)  return '🌑';
    if (d <= 6)  return '🌒';
    if (d <= 12) return '🌓';
    if (d <= 14) return '🌕';
    if (d <= 20) return '🌖';
    if (d <= 25) return '🌗';
    if (d <= 28) return '🌘';
    return '🌑';
}

