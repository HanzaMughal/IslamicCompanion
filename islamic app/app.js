// State for Azkar
let activeCategory = localStorage.getItem('last_azkar_cat') || 'morning_azkar';

/**
 * Renders the Azkar Cards dynamically
 */
function renderAzkar(catId) {
    activeCategory = catId;
    localStorage.setItem('last_azkar_cat', catId);

    const container = document.getElementById('azkar-container');
    const items = AZKAR_DATA[catId];

    container.innerHTML = items.map(item => {
        // Get saved progress from LocalStorage
        const storageKey = `zikr_${catId}_${item.id}`;
        const currentProgress = parseInt(localStorage.getItem(storageKey)) || 0;
        const isCompleted = currentProgress >= item.count;

        return `
        <div class="zikr-card ${isCompleted ? 'completed' : ''}" id="card-${catId}-${item.id}">
            <div class="zikr-main-content" onclick="toggleExpand(this)">
                <p class="arabic-text">${item.arabic}</p>
                <div class="expandable-content">
                    <p class="urdu-text">${item.urdu}</p>
                    <p class="english-text">${item.english}</p>
                </div>
            </div>
            
            <div class="zikr-footer">
                <div class="progress-info">
                    <span class="count-display">${currentProgress}/${item.count}</span>
                    <div class="progress-bar-bg">
                        <div class="progress-bar-fill" style="width: ${(currentProgress / item.count) * 100}%"></div>
                    </div>
                </div>
                <div class="zikr-actions">
                    <button class="reset-btn" onclick="resetZikr('${catId}', ${item.id})">
                        <i class="fas fa-undo"></i>
                    </button>
                    <button class="count-btn" onclick="incrementZikr('${catId}', ${item.id}, ${item.count})" ${isCompleted ? 'disabled' : ''}>
                        ${isCompleted ? '<i class="fas fa-check"></i>' : 'TAP'}
                    </button>
                </div>
            </div>
        </div>
        `;
    }).join('');
}

/**
 * Increments the counter for a specific Zikr
 */
function incrementZikr(catId, itemId, targetCount) {
    const storageKey = `zikr_${catId}_${itemId}`;
    let current = parseInt(localStorage.getItem(storageKey)) || 0;

    if (current < targetCount) {
        current++;
        localStorage.setItem(storageKey, current);

        // Haptic Feedback
        if ("vibrate" in navigator) navigator.vibrate(40);

        // Audio Feedback (Optional)
        // playSoftTapSound();

        // Refresh UI for this card only for performance
        refreshZikrUI(catId, itemId, current, targetCount);
    }
}

function resetZikr(catId, itemId) {
    const storageKey = `zikr_${catId}_${itemId}`;
    localStorage.setItem(storageKey, 0);
    refreshZikrUI(catId, itemId, 0, 1); // targetCount handled in refresh
}

function refreshZikrUI(catId, itemId, current, target) {
    const card = document.getElementById(`card-${catId}-${itemId}`);
    const items = AZKAR_DATA[catId];
    const actualTarget = items.find(i => i.id === itemId).count;

    const countDisplay = card.querySelector('.count-display');
    const bar = card.querySelector('.progress-bar-fill');
    const btn = card.querySelector('.count-btn');

    countDisplay.innerText = `${current}/${actualTarget}`;
    bar.style.width = `${(current / actualTarget) * 100}%`;

    if (current >= actualTarget) {
        card.classList.add('completed');
        btn.innerHTML = '<i class="fas fa-check"></i>';
        btn.disabled = true;
    } else {
        card.classList.remove('completed');
        btn.innerHTML = 'TAP';
        btn.disabled = false;
    }
}

function toggleExpand(element) {
    element.parentElement.classList.toggle('expanded');
}

// Map the UI tab clicks
function filterAzkar(type) {
    // UI mapping for tab IDs to data object keys
    const catMap = {
        'morning': 'morning_azkar',
        'evening': 'evening_azkar',
        'after-prayer': 'after_salah_azkar'
    };
    renderAzkar(catMap[type]);

    // Update active tab styles
    document.querySelectorAll('.cat-chip').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('onclick').includes(type));
    });
}
// --- Navigation Logic ---
function navigateTo(pageId) {
    // Hide all pages
    document.querySelectorAll('.page').forEach(page => {
        page.classList.remove('active');
    });

    // Show target page
    const targetPage = document.getElementById(pageId);
    if (targetPage) {
        targetPage.classList.add('active');
    }

    // Update bottom nav active state
    document.querySelectorAll('.nav-item').forEach(item => {
        item.classList.toggle('active', item.getAttribute('onclick').includes(pageId));
    });

    // Special case for Azkar: render default category if needed
    if (pageId === 'azkar') {
        renderAzkar(activeCategory);
    }
}

// --- Theme Toggle Logic ---
const themeSettingsToggle = document.getElementById('dark-mode-toggle-settings');

function setTheme(theme) {
    document.documentElement.setAttribute('data-theme', theme);
    localStorage.setItem('theme', theme);
    if (themeSettingsToggle) themeSettingsToggle.checked = (theme === 'dark');
}

if (themeSettingsToggle) {
    themeSettingsToggle.addEventListener('change', (e) => {
        setTheme(e.target.checked ? 'dark' : 'light');
    });
}

